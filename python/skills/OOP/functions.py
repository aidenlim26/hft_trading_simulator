# One argument
def greet_user(name):
    print(f'Hi {name}')
    print('Welcome aboard!')

print('Start')
greet_user("John Smith")

# Two arguments
def greet_user(first_name, last_name):
    print(f'Hi {first_name} {last_name}')
    print('Welcome aboard!')

print('Start')
greet_user("John", "Smith")


